    @Test
    @Transactional
    void createComment() throws Exception {
        int databaseSizeBeforeCreate = commentRepository.findAll().size();
        // Create the Comment
        CommentDTO commentDTO = commentMapper.toDto(comment);
        restCommentMockMvc
            .perform(
                post(ENTITY_API_URL)
                    .with(csrf())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(commentDTO))
            )
            .andExpect(status().isCreated());

        // Validate the Comment in the database
        List<Comment> commentList = commentRepository.findAll();
        assertThat(commentList).hasSize(databaseSizeBeforeCreate + 1);
        Comment testComment = commentList.get(commentList.size() - 1);
        assertThat(testComment.getText()).isEqualTo(DEFAULT_TEXT);
        assertThat(testComment.getCommentTime()).isEqualTo(DEFAULT_COMMENT_TIME);
        assertThat(testComment.getCreatedBy()).isEqualTo(DEFAULT_CREATED_BY);
        assertThat(testComment.getImageUrl()).isEqualTo(DEFAULT_IMAGE_URL);
        assertThat(testComment.getReplay()).isEqualTo(DEFAULT_REPLAY);
    }

